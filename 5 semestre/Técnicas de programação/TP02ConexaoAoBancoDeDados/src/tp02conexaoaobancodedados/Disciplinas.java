    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
            umaDisciplina.setCodigo(jTextField1.getText());
            DisciplinasBLL.validaCodigo(umaDisciplina);
            if (Erro.getErro())
                JOptionPane.showMessageDialog(null,Erro.getMens());
            else
            {
                DisciplinasBLL.deletaDisciplina(umaDisciplina);
                if (Erro.getErro())
                    JOptionPane.showMessageDialog(null,Erro.getMens());
                else
                    JOptionPane.showMessageDialog(null,"Deletado com sucesso");
            }   
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
                umaDisciplina.setCodigo(jTextField1.getText());
                umaDisciplina.setNome(jTextField2.getText());
                umaDisciplina.setCargaHoraria(jTextField3.getText());
                DisciplinasBLL.validaDadosParaUpdate(umaDisciplina);
        if (Erro.getErro())
           JOptionPane.showMessageDialog(null,Erro.getMens());
        else
        {
            DisciplinasBLL.atualizaDisciplina(umaDisciplina);
            JOptionPane.showMessageDialog(null,"Editaado com sucesso");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

