    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        umaDisciplina.setCodigo(jTextField1.getText());
        DisciplinasBLL.validaCodigo(umaDisciplina);
        if (Erro.getErro())
            JOptionPane.showMessageDialog(null,Erro.getMens());
        else
        {
            jTextField1.setText(umaDisciplina.getCodigo());
            jTextField2.setText(umaDisciplina.getNome());
            jTextField3.setText(umaDisciplina.getCargaHoraria());
        }    
    }//GEN-LAST:event_jButton2ActionPerformed

